package de.unisaarland.cs.se.selab.assets

/**
 * Represents the different types of garbage that can be collected in the simulation.
 */
enum class GarbageType {
    /**
     * Garbage type for plastic waste.
     */
    PLASTIC,

    /**
     * Garbage type for oil waste.
     */
    OIL,

    /**
     * Garbage type for chemical waste.
     */
    CHEMICALS,

    /**
     * Represents no garbage.
     */
    NONE
}
