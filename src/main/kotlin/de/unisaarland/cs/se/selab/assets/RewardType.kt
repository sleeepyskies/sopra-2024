package de.unisaarland.cs.se.selab.assets

/**
 * Represents the different types of rewards that can be given in the simulation.
 */
enum class RewardType {
    /**
     * Reward type for a telescope.
     */
    TELESCOPE,

    /**
     * Reward type for a radio.
     */
    RADIO,

    /**
     * Reward type for tracking equipment.
     */
    TRACKING,

    /**
     * Reward type for a container.
     */
    CONTAINER
}
