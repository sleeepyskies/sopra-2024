package de.unisaarland.cs.se.selab.assets

/**
 * Represents the different types of tiles in the simulation.
 */
enum class TileType {
    /**
     * Tile type for land.
     */
    LAND,

    /**
     * Tile type for shore.
     */
    SHORE,

    /**
     * Tile type for shallow ocean.
     */
    SHALLOW_OCEAN,

    /**
     * Tile type for deep ocean.
     */
    DEEP_OCEAN
}
